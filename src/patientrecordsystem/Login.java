    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (txtempNo.getText().isEmpty() || txtpassword.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Employee number or password cannot be empty!!");
        } else if (jComboBox1.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(null, "You must choose a usertype!!");
        } else {

            try {
                String query = "select * from users where empNo=? and empPassword=? and userType=? ";
                con = DriverManager.getConnection("jdbc:mysql://localhost:3306/prs", "root", "");
                pst = con.prepareStatement(query);
                pst.setString(1, txtempNo.getText());
                pst.setString(2, txtpassword.getText());
                pst.setString(3, String.valueOf(jComboBox1.getSelectedItem()));
                rs = pst.executeQuery();
                if (rs.next()) {

                    if (jComboBox1.getSelectedIndex() == 0) {
                        Admin a = new Admin();
                        a.setVisible(true);
                        this.dispose();
                    } else if (jComboBox1.getSelectedIndex() == 1) {
                        Receptionist r1 = new Receptionist();
                        r1.setVisible(true);
                        this.dispose();
                    } else if (jComboBox1.getSelectedIndex() == 2) {
                        Doctor d1 = new Doctor();
                        d1.setVisible(true);
                        this.dispose();
                    } else if (jComboBox1.getSelectedIndex() == 3) {
                        Pharmacist p1 = new Pharmacist();
                        p1.setVisible(true);
                        this.dispose();
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "employeeNo  and password do not match!");
                }
            } catch (SQLException ex) {
                ex.printStackTrace();
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

