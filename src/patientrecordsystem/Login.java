    private void txtempNoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtempNoKeyPressed
        // TODO add your handling code here:
        char x = evt.getKeyChar();
        if (Character.isLetter(x)) {
            //can't enter a textfield if entered character is not a number
            txtempNo.setEditable(false);
            //error message
            JOptionPane.showMessageDialog(null, "Please enter a number only!!");
        } else {
            txtempNo.setEditable(true);

        }


    }//GEN-LAST:event_txtempNoKeyPressed

    private void txtpasswordKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtpasswordKeyPressed
        // TODO add your handling code here:
        char l1 = evt.getKeyChar();
        if (Character.isISOControl(l1) || Character.isLetterOrDigit(l1)) {
            //accept only letter and digits
            txtpassword.setEditable(true);

        } else {
            // deny character
            txtpassword.setEditable(false);
            //error message
            JOptionPane.showMessageDialog(null, "Please enter letters or digits only!!");
        }
    }//GEN-LAST:event_txtpasswordKeyPressed

    private void txtpasswordKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtpasswordKeyReleased
        // TODO add your handling code here:
        int position1 = txtpassword.getCaretPosition();
        txtpassword.setText(txtpassword.getText().toLowerCase());
        txtpassword.setCaretPosition(position1);
    }//GEN-LAST:event_txtpasswordKeyReleased

